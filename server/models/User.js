const mongoose = require('mongoose');

const {
  Schema,
} = mongoose;

// Create Schema
const UserSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
  },
  password: {
    type: String,
    required: true,
  },
  isAdmin: {
    type: Boolean,
    required: true,
    default: false,
  },
});

// Create a model
const user = mongoose.model('users', UserSchema);

// Export the model
module.exports = user;
